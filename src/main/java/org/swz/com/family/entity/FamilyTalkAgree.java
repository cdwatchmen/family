package org.swz.com.family.entity;

import java.util.Date;

public class FamilyTalkAgree {
	private String agreeId;
	private String talkId;
	private Date agreeTime;
	private String agreeUserId;
	private String agreeUserNick;
	
	
	public String getAgreeId() {
		return agreeId;
	}
	public void setAgreeId(String agreeId) {
		this.agreeId = agreeId;
	}
	public String getTalkId() {
		return talkId;
	}
	public void setTalkId(String talkId) {
		this.talkId = talkId;
	}
	public Date getAgreeTime() {
		return agreeTime;
	}
	public void setAgreeTime(Date agreeTime) {
		this.agreeTime = agreeTime;
	}
	public String getAgreeUserId() {
		return agreeUserId;
	}
	public void setAgreeUserId(String agreeUserId) {
		this.agreeUserId = agreeUserId;
	}
	public String getAgreeUserNick() {
		return agreeUserNick;
	}
	public void setAgreeUserNick(String agreeUserNick) {
		this.agreeUserNick = agreeUserNick;
	}
	
	
	

}
