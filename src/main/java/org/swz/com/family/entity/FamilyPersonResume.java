package org.swz.com.family.entity;

public class FamilyPersonResume {
	
	private String resumeId;
	private String resumeDynasty;
	private String resumeStartTime;
	private String resumeEndTime;
	private String resumeContent;
	private String createUserId;
	private int honoraryDegree;
	private int verifyStatus;
	private String personId; 
	
	
	private String fullName;
	
	
	
	
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public int getHonoraryDegree() {
		return honoraryDegree;
	}
	public void setHonoraryDegree(int honoraryDegree) {
		this.honoraryDegree = honoraryDegree;
	}
	public String getResumeId() {
		return resumeId;
	}
	public void setResumeId(String resumeId) {
		this.resumeId = resumeId;
	}
	public String getResumeDynasty() {
		return resumeDynasty;
	}
	public void setResumeDynasty(String resumeDynasty) {
		this.resumeDynasty = resumeDynasty;
	}
	public String getResumeStartTime() {
		return resumeStartTime;
	}
	public void setResumeStartTime(String resumeStartTime) {
		this.resumeStartTime = resumeStartTime;
	}
	public String getResumeEndTime() {
		return resumeEndTime;
	}
	public void setResumeEndTime(String resumeEndTime) {
		this.resumeEndTime = resumeEndTime;
	}
	public String getResumeContent() {
		return resumeContent;
	}
	public void setResumeContent(String resumeContent) {
		this.resumeContent = resumeContent;
	}
	public String getCreateUserId() {
		return createUserId;
	}
	public void setCreateUserId(String createUserId) {
		this.createUserId = createUserId;
	}
	public String getPersonId() {
		return personId;
	}
	public void setPersonId(String personId) {
		this.personId = personId;
	}
	public int getVerifyStatus() {
		return verifyStatus;
	}
	public void setVerifyStatus(int verifyStatus) {
		this.verifyStatus = verifyStatus;
	}   
	
	
}
